// Helper functions
export const getProvinces = () => {
  return vietnamAdministrative.provinces;
};

export const getDistrictsByProvince = (provinceCode) => {
  return vietnamAdministrative.districts[provinceCode] || [];
};

export const getWardsByDistrict = (districtCode) => {
  return vietnamAdministrative.wards[districtCode] || [];
};

export const getProvinceByCode = (code) => {
  return vietnamAdministrative.provinces.find(p => p.code === code);
};

export const getDistrictByCode = (provinceCode, districtCode) => {
  const districts = vietnamAdministrative.districts[provinceCode] || [];
  return districts.find(d => d.code === districtCode);
};

export const getWardByCode = (districtCode, wardCode) => {
  const wards = vietnamAdministrative.wards[districtCode] || [];
  return wards.find(w => w.code === wardCode);
};

// Utility function to get full address string
export const getFullAddress = (provinceCode, districtCode, wardCode, streetName = '', houseNumber = '') => {
  const province = getProvinceByCode(provinceCode);
  const district = getDistrictByCode(provinceCode, districtCode);
  const ward = getWardByCode(districtCode, wardCode);
  
  const parts = [];
  if (houseNumber) parts.push(houseNumber);
  if (streetName) parts.push(streetName);
  if (ward) parts.push(ward.name);
  if (district) parts.push(district.name);
  if (province) parts.push(province.name);
  
  return parts.join(', ');
};

// Function to search provinces by name
export const searchProvinces = (searchTerm) => {
  if (!searchTerm) return vietnamAdministrative.provinces;
  
  return vietnamAdministrative.provinces.filter(province => 
    province.name.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

// Function to search districts by name within a province
export const searchDistricts = (provinceCode, searchTerm) => {
  const districts = getDistrictsByProvince(provinceCode);
  if (!searchTerm) return districts;
  
  return districts.filter(district => 
    district.name.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

// Function to search wards by name within a district
export const searchWards = (districtCode, searchTerm) => {
  const wards = getWardsByDistrict(districtCode);
  if (!searchTerm) return wards;
  
  return wards.filter(ward => 
    ward.name.toLowerCase().includes(searchTerm.toLowerCase())
  );
};